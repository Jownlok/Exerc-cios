num1 = int(input('Digite um número inteiro: '))
num2 = int(input('Digite outro número inteiro: '))
intervalo1 = 0
intervalo2 = 0

for num1 in range(intervalo1, num1,1):
    intervalo1 = intervalo1 + 1
    print(f'{intervalo1}, ', end='')

for num2 in range(intervalo2, num2,1):
    intervalo2 = intervalo2 + 1
    print(f'{intervalo2}, ', end='')

print('Encerrando. . .')